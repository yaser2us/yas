    private void btnSearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSearchActionPerformed
        if(!txtID.getText().isEmpty()){
            Emp xEmployee = xEmployees.getEmployee(Integer.parseInt(txtID.getText()));
            if (xEmployee.Salary != 0){
                lblInformation.setText("The record is found");
                txtName.setText(xEmployee.Name);
                txtEmail.setText(xEmployee.Email);
                txtContact.setText(xEmployee.Contact);
                txtBenefits.setText(Double.toString(xEmployee.Benefit));
                txtSalary.setText(Integer.toString(xEmployee.Salary));
                chbLeave.setSelected(xEmployee.Leave);
                btnSave.setEnabled(true);
                xEmployees.RefreshTable(aEmployeeModel, tblEmployees);
            } else {
                lblInformation.setText("The record is not found");                
                txtName.setText("");
                txtEmail.setText("");
                txtContact.setText("");
                txtBenefits.setText("");
                txtSalary.setText("");
                chbLeave.setSelected(false);            
                btnSave.setEnabled(false);
            }
        }
    }//GEN-LAST:event_btnSearchActionPerformed

