    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (!txtID.getText().isEmpty()) {
            lblInformation.setText(xEmployees.Delete(Integer.parseInt(txtID.getText())));
            xEmployees.RefreshTable(aEmployeeModel, tblEmployees);
        }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        if(!txtID.getText().isEmpty()){
             Emp updateEmployee = new Staff(Integer.parseInt(txtID.getText()),txtName.getText(), txtEmail.getText(), txtContact.getText(), Integer.parseInt(txtSalary.getText()),Double.parseDouble(txtBenefits.getText()), chbLeave.isSelected());
             lblInformation.setText(xEmployees.Update(updateEmployee));    
             xEmployees.RefreshTable(aEmployeeModel, tblEmployees);
         }
    }//GEN-LAST:event_btnSaveActionPerformed

    private void btnNewActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNewActionPerformed
        if (txtID.getText().isEmpty()) {
            Emp newEmployee = new Staff(txtName.getText(), txtEmail.getText(), txtContact.getText(), Integer.parseInt(txtSalary.getText()),Double.parseDouble(txtBenefits.getText()), chbLeave.isSelected());            
            xEmployees.Insert(newEmployee);
            xEmployees.RefreshTable(aEmployeeModel, tblEmployees);
        }
    }//GEN-LAST:event_btnNewActionPerformed

